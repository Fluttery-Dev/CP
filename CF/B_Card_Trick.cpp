void solve()
{
    int n;
    cin >> n;
    int arr[n];
    for (int i = 0; i < n; i++)
    {
        cin >> arr[i];
    }

    int q;
    cin >> q;
    int sum = 0;
    int t;
    for (int i = 0; i < q; i++)
    {
        cin >> t;
        sum += t;
    }

    sum %= n;
    cout << arr[sum];
}

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(NULL);
    int tc;
    cin >> tc;
    while (tc--)
    {
        solve();
        cout << endl;
    }
}